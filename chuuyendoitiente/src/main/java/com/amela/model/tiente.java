package com.amela.model;

public class tiente {
    private int usd;
    private int tigia;

    public tiente() {
    }

    public tiente(int usd, int tigia) {
        this.usd = usd;
        this.tigia = tigia;
    }

    public int getUsd() {
        return usd;
    }

    public void setUsd(int usd) {
        this.usd = usd;
    }

    public int getTigia() {
        return tigia;
    }

    public void setTigia(int tigia) {
        this.tigia = tigia;
    }
}
